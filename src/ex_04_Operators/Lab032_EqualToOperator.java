package ex_04_Operators;

public class Lab032_EqualToOperator {
    public static void main(String[] args) {

        // == compares the values

        System.out.println(10==10);
        System.out.println(10>20);
        System.out.println(10==20);
        System.out.println(10<=10);
        System.out.println(10>=10);
    }
}
