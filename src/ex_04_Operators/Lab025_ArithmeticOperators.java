package ex_04_Operators;

public class Lab025_ArithmeticOperators {
    public static void main(String[] args) {

        int a=20;
        int b=3;
        float c=3f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b); //gives integer output
        System.out.println(a/c); //gives float output
    }
}
