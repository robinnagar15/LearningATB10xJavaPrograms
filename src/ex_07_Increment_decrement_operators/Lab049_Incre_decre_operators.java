package ex_07_Increment_decrement_operators;

public class Lab049_Incre_decre_operators {
    public static void main(String[] args) {

        //Pre-increment -> first increment then store
        // ++ -> operand

        int a=10;
        int b=++a;

        System.out.println(b);
        System.out.println(a);
    }
}
